// 1. Number Type
let num1 = 27;
let num2 = 1.5;
let num3 = -20;

let inf = Infinity;
let mInf = -Infinity;

// 수치 연산이 실패했을 때의 결과값
let nan = NaN;

// 2. String Type
let myName = "이정환";
let myLocation = "목동";
let introduce = myName + myLocation;

// 템플릿 리터럴 문법
let introduceText = `${myName}은 ${myLocation}에 거주합니다.`;

// 3. Boolean Type
let isSwitchOn = true;
let isEmpty = false;

// 4. Null Type = 아무것도 없다.
// 명시적으로 할당해줘야 하는 값
let empty = null;

// 5. Undefined Type
// 변수에 아무 값도 할당하지 않았을 때 자동으로 할당
let none;
